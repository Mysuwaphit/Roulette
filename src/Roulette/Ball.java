package Roulette;


public class Ball {

    private int number;
    private int even;

    /**
     *
     * @return number
     */
    public int getNumber() {
        return number;
    }

    /**
     *
     * @param number
     */
    public void setNumber(int number) {
        this.number = number;
    }

    /**
     *
     * @return even
     */
    public int isEven() {
        return even;
    }

    /**
     *
     * @param even
     */
    public void setEven(int even) {
        this.even = even;
    }
    
}
